#include "libft.h"

void	ft_strtabdel(char ***tab)
{
	int		i;

	if (tab && *tab)
	{
		i = 0;
		while ((*tab)[i])
		{
			ft_strdel(&(*tab)[i]);
			++i;
		}
		free(*tab);
		*tab = NULL;
	}
}
