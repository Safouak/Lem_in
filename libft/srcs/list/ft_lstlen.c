#include "libft.h"

int		ft_lstlen(t_list *alst)
{
	int		i;
	t_list	*tmp;

	i = 0;
	tmp = alst;
	while (tmp)
	{
		++i;
		tmp = tmp->next;
	}
	return (i);
}
